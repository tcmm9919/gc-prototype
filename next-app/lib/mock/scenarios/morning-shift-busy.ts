const now = new Date()
const ago = (mins: number) =>
  new Date(now.getTime() - mins * 60_000).toISOString()
const inFuture = (mins: number) =>
  new Date(now.getTime() + mins * 60_000).toISOString()

// Pipeline-хелпер: типы шагов берём из WORKFLOW_ACTIVITIES (sanction_check,
// behavior_scoring, ocr_document, send_email, generate_report, news_search...)
const pipe = (
  sc: string,
  steps: Array<[string, Record<string, string | boolean>]>
): WorkflowStep[] =>
  steps.map(([type, config], idx) => ({ id: `${sc}-s${idx}`, type, config }))

