const now = Date.now();
const hours = (h: number) => new Date(now - h * 3600_000).toISOString();
const days = (d: number) => new Date(now - d * 86_400_000).toISOString();

/**
 * Helper: fills in compliance-platform fields (purposeCode, branchCode etc.) so
 * story transactions only need to specify the narrative-relevant bits.
 */
function tx(partial: Omit<Transaction, "purposeCode" | "purposeDescription" | "branchCode" | "branchName" | "priority" | "complianceStatus"> &
  Partial<Pick<Transaction, "purposeCode" | "purposeDescription" | "branchCode" | "branchName" | "priority" | "complianceStatus">>): Transaction {
  return {
    purposeCode: "119",
    purposeDescription: "Прочие безвозмездные переводы денег физическим лицам",
    branchCode: "ALM-001",
    branchName: "Центральный филиал, Алматы",
    priority: partial.riskLevel === "critical" || partial.riskLevel === "high" ? "high" : partial.riskLevel === "medium" ? "medium" : "low",
    complianceStatus: partial.status === "blocked" ? "Отклонена" : partial.status === "review" ? "Ожидание" : "Завершена",
    ...partial,
  };
}

